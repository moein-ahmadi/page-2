// header
const pagesClick = () => {
  const x = document.querySelector(".header--li--divHidden");
  if (x.style.display === "none") {
    x.style.display = "block";
  } else {
    x.style.display = "none";
  }
};
// -----------------------
const menuClick = () => {
  const m = document.querySelector(".header-div--nav");
  if (m.style.display === "none") {
    m.style.display = "block";
  } else {
    m.style.display = "none";
  }
};
